package poly.child;

import poly.parent.Person;

public class Teacher extends Person {
	private double salary;

	public Teacher(String name, String personId, String address, String phoneNumber, double salary) {
		super(name, personId, address, phoneNumber);
		this.salary = salary;
	}

	public double getSalary() {
		return salary;
	}

	public void setSalary(double salary) {
		this.salary = salary;
	}

	@Override
	public String toString() {
		return super.toString() + "Teacher [salary=" + salary + "]";
	}

}
